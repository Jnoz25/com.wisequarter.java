package day15_methodOverloading_whileLoop;

public class C03_WhileLoop {
    public static void main(String[] args) {

        for (int i = 1; i <= 10; i++) {

            System.out.print(i + " ");
        }
        System.out.println(" ");

        int sayi = 1;

        while (sayi<=10) {
            System.out.print(sayi +" * ");
            sayi++;
        }
    }
}
