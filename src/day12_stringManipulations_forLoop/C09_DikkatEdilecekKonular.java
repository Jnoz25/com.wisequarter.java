package day12_stringManipulations_forLoop;

public class C09_DikkatEdilecekKonular {
    public static void main(String[] args) {

        for (int i = 10; i < 0 ; i++) {
            System.out.println(i);

        }

        for (int i = 0; i < 10; i--) {

            System.out.println(i);

        }
    }
}
